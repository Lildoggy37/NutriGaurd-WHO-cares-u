def load_checkpoint() -> dict:
    """加载进度文件"""
    if CHECKPOINT_PATH.exists():
        return json.loads(CHECKPOINT_PATH.read_text(encoding="utf-8"))
    return {"completed_sections": [], "total_chunks_estimate": 0}


def save_checkpoint(checkpoint: dict):
    """保存进度"""
    CHECKPOINT_PATH.parent.mkdir(parents=True, exist_ok=True)
    CHECKPOINT_PATH.write_text(json.dumps(checkpoint, ensure_ascii=False, indent=2), encoding="utf-8")


async def generate_section(
    llm: ChatOpenAI,
    chapter_title: str,
    section_title: str,
    section_index: int,
    total_sections: int,
) -> Optional[str]:
    """生成单个 section 的内容"""
    user_prompt = f"""请撰写以下章节中的一节：

所属章节：{chapter_title}
本节标题：{section_title}
本节序号：第 {section_index+1}/{total_sections} 节

请直接以 ### {section_title} 开头，撰写3000-5000字的专业内容（这是正式医学指南，不是科普文章，请务必详实）。包含：
- 至少1个markdown表格（营养数据/食物清单/剂量建议）
- 至少2个具体的食谱示例或实操方案
- 相关循证医学证据等级标注"""

    for attempt in range(MAX_RETRIES):
        try:
            response = await llm.ainvoke([
                SystemMessage(content=SYSTEM_PROMPT),
                HumanMessage(content=user_prompt),
            ])
            content = str(response.content) if response.content else ""
            if len(content) < 800:
                print(f"  [WARN] 内容过短({len(content)}字)，重试 {attempt+1}/{MAX_RETRIES}")
                await asyncio.sleep(3)
                continue
            return content
        except Exception as e:
            print(f"  [FAIL] 生成失败 (attempt {attempt+1}): {e}")
            if attempt < MAX_RETRIES - 1:
                await asyncio.sleep(5 * (attempt + 1))
            else:
                return None
    return None


async def generate_chapter(
    llm: ChatOpenAI,
    chapter: dict,
    chapter_index: int,
    checkpoint: dict,
) -> tuple[int, list[str]]:
    """生成一个章节的所有 section，返回 (新增chunk估算数, 生成的内容列表)"""
    chapter_title = chapter["title"]
    sections = chapter["sections"]
    total = len(sections)
    generated = []
    new_chunks = 0

    print(f"\n{'='*60}")
    print(f"第 {chapter_index+1} 章: {chapter_title.strip('# ')}")
    print(f"共 {total} 节")
    print(f"{'='*60}")

    for i, section_title in enumerate(sections):
        section_id = hashlib.md5(f"{chapter_title}||{section_title}".encode()).hexdigest()[:12]
        if section_id in checkpoint["completed_sections"]:
            print(f"  [{i+1}/{total}] [OK] (已跳过) {section_title}")
            continue

        print(f"  [{i+1}/{total}] 生成中: {section_title[:60]}...", end=" ", flush=True)
        content = await generate_section(llm, chapter_title, section_title, i, total)

        if content:
            generated.append(content)
            checkpoint["completed_sections"].append(section_id)
            est_chunks = max(1, len(content) // 384)  # 512ch - 128ov
            new_chunks += est_chunks
            checkpoint["total_chunks_estimate"] += est_chunks
            save_checkpoint(checkpoint)
            print(f"[OK] ({len(content)}字, ~{est_chunks} chunks)")
        else:
            print(f"[FAIL] 失败，跳过")
            # 标记为完成（避免卡住），但记录为空
            checkpoint["completed_sections"].append(section_id)
            save_checkpoint(checkpoint)

        await asyncio.sleep(BATCH_DELAY)

    return new_chunks, generated


async def main():
    parser = argparse.ArgumentParser(description="NutriGuard 语料批量扩充工具")
    parser.add_argument("--dry-run", action="store_true", help="只打印计划，不调用 LLM")
    parser.add_argument("--chapter", type=int, default=0, help="只生成指定章节 (1-indexed)")
    parser.add_argument("--reset", action="store_true", help="清除进度，从头开始")
    args = parser.parse_args()

    if args.reset and CHECKPOINT_PATH.exists():
        CHECKPOINT_PATH.unlink()
        print("[OK] 已清除进度文件")

    checkpoint = load_checkpoint()
    print(f"模型: {MODEL}")
    print(f"已完成的 section: {len(checkpoint['completed_sections'])}")
    print(f"当前估算 chunks: {checkpoint['total_chunks_estimate']}")
    print(f"目标: ~10000 chunks")

    if args.dry_run:
        total_sections = sum(len(ch["sections"]) for ch in CHAPTERS)
        print(f"\n[Dry Run] 共 {len(CHAPTERS)} 章, {total_sections} 节待生成")
        for i, ch in enumerate(CHAPTERS):
            print(f"  {i+1}. {ch['title'].strip('# ')} ({len(ch['sections'])} 节)")
        return

    # 初始化 LLM（DeepSeek）
    print(f"模型: {MODEL} (DeepSeek)")
    print(f"API Base: {LLM_KWARGS['base_url']}")
    llm = ChatOpenAI(**LLM_KWARGS)

    # 读取现有语料
    existing_corpus = ""
    if CORPUS_PATH.exists():
        existing_corpus = CORPUS_PATH.read_text(encoding="utf-8")

    total_new_chunks = 0
    all_new_content: list[str] = []

    chapters_to_generate = CHAPTERS
    if args.chapter > 0:
        chapters_to_generate = [CHAPTERS[args.chapter - 1]]

    for i, chapter in enumerate(chapters_to_generate):
        chapter_idx = args.chapter - 1 if args.chapter > 0 else i
        new_chunks, content_list = await generate_chapter(llm, chapter, chapter_idx, checkpoint)
        total_new_chunks += new_chunks
        all_new_content.extend(content_list)

        # 每章生成完成后追加写入
        if all_new_content:
            chapter_header = f"\n\n---\n\n{chapter['title']}\n\n> {chapter['description']}\n\n"
            new_text = chapter_header + "\n\n".join(all_new_content[-len(content_list):])
            with open(CORPUS_PATH, "a", encoding="utf-8") as f:
                f.write(new_text)
            print(f"  → 已写入 {CORPUS_PATH}")

    # 最终统计
    total_file_chars = CORPUS_PATH.read_text(encoding="utf-8").__len__() if CORPUS_PATH.exists() else 0
    print(f"\n{'='*60}")
    print(f"扩充完成!")
    print(f"  新增 chunks (估算): {total_new_chunks}")
    print(f"  累计 chunks (估算): {checkpoint['total_chunks_estimate']}")
    print(f"  语料总字符数: {total_file_chars:,}")
    print(f"  语料文件: {CORPUS_PATH}")
    print(f"{'='*60}")


if __name__ == "__main__":
    asyncio.run(main())
